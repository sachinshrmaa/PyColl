
#To fing the area of Square
def area_square(side):
    area = side * side
    return print(area)

#To fing the area of Rectangle
def area_rectangle(length, breadth):
    area = length * breadth
    return print(area)

#To fing the area of Triangle
def area_triangle(base, height):
    area = 1/2 * base * height
    return print(area)

#To fing the area of Parallelogram
def area_parallelogram(base, height):
    area =  base * height
    return print(area)

#To fing the area of Rhombus
def area_rhombus(diagonal, diagonal):
    area = diagonal * diagonal /2
    return print(area)
